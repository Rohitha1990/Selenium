package basicloops;

public class ForLoop {

	public void m1()
	{
		System.out.println("I am method m1");
	}
	public static void main(String[] args) {
		
		/*local variables declaration*/
		int n=1, times=5;
		
		
		for(n=1; n<=times; n=n+1)
		{
			System.out.println("Java for loops :" +n);
		}
		

	}

}
