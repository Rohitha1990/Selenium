package basicdecisionmaking;

public class ElseIfDecisionMaking {

	public static void main(String[] args)
	{
		int a=30, b=30;
		
		if(a<b)
		{
			System.out.println("a is greater");
		}
		else if(b>a)
		{
			System.out.println("b is greater");
		}
		else
		{
			System.out.println("a is equal to b");
		}

	}

}
